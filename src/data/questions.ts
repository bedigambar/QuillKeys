export const getQuestionsByContentType = (contentType: 'prose' | 'poetry'): Question[] => {
  return questions.filter(q => q.contentType === contentType);
};

export const getCategoriesByContentType = (contentType: 'prose' | 'poetry'): string[] => {
  const categories = questions
    .filter(q => q.contentType === contentType)
    .map(q => q.category);
  return Array.from(new Set(categories));
};

export const getQuestionsByCategory = (category: string): Question[] => {
  return questions.filter(q => q.category === category);
};

export const getRandomQuestion = (category: string): Question => {
  const categoryQuestions = getQuestionsByCategory(category);
  const randomIndex = Math.floor(Math.random() * categoryQuestions.length);
  return categoryQuestions[randomIndex];
};

export const getContentTypeByCategory = (category: string): 'prose' | 'poetry' => {
  const question = questions.find(q => q.category === category);
  return question ? question.contentType : 'prose';
};
